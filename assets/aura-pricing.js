/* AURA PRINT & PROMO - shared live-price configurator.
   Generalisation of the business-card pattern (bc-configurator.js) for any
   product with a row set in Supabase public.product_prices.

   PRICING IS LIVE. Prices are read at page load from product_prices
   (anon SELECT only, RLS). No prices live in this file or in any page.
   Wholesale cost + supplier live in the staff-only product_price_audit table
   and never reach the browser. Rows are generated by
   pricing-engine/tools/generate-product-prices.py (single source of truth).

   source: real = reconciled against an actual supplier invoice.
           estimate = derived from harvested supplier rates; shown as an
           indicative price, confirmed on the proof before payment.

   Missing combination or feed failure -> POA + quote link. Never invent a price.

   Usage on a product page:
     <div id="aura-config" data-product="corflute-signs"
          data-axes='[["size","Size",["600x450mm","600x900mm","900x600mm","900x1200mm","1200x900mm"]],
                      ["thickness","Thickness",["3mm","5mm"]],
                      ["sides","Sides",["Single","Double"]]]'
          data-default='{"size":"900x600mm","thickness":"5mm","sides":"Single"}'
          data-qty="1"></div>
     <script src="assets/aura-pricing.js"></script>

   Quantity buttons adapt to whatever breaks exist for the selected combo. */
(function () {
  var CFG = window.AURA_CONFIG || {};
  var host = document.getElementById('aura-config');
  if (!host) return;

  var SLUG = host.getAttribute('data-product');
  var AXES = JSON.parse(host.getAttribute('data-axes') || '[]');           // [key, label, [values]]
  var DEF  = JSON.parse(host.getAttribute('data-default') || '{}');
  var startQty = parseInt(host.getAttribute('data-qty'), 10) || 0;

  var GRID = null;        // key -> { qty: {cents, source} }
  var loadFailed = false;

  function comboKey(sel) {
    return AXES.map(function (a) { return sel[a[0]]; }).join('|');
  }
  function currentSel() {
    var sel = {};
    AXES.forEach(function (a) {
      var b = host.querySelector('.opts[data-group="' + a[0] + '"] .on');
      sel[a[0]] = b ? b.dataset.val : DEF[a[0]];
    });
    return sel;
  }
  function currentQty() {
    var b = host.querySelector('.opts[data-group="qty"] .on');
    return b ? parseInt(b.dataset.val, 10) : startQty;
  }
  function nearest(target, arr) {
    return arr.reduce(function (a, b) { return Math.abs(b - target) < Math.abs(a - target) ? b : a; });
  }

  function optBtns(group, values, activeVal) {
    return '<div class="opts" data-group="' + group + '">' + values.map(function (v) {
      return '<button class="opt' + (String(v) === String(activeVal) ? ' on' : '') + '" data-val="' + v + '">' + v + '</button>';
    }).join('') + '</div>';
  }

  host.innerHTML =
    AXES.map(function (a) {
      return '<div class="optlabel">' + a[1] + '</div>' + optBtns(a[0], a[2], DEF[a[0]]);
    }).join('') +
    '<div class="optlabel">Quantity</div><div class="opts" data-group="qty" id="qtyGroup"></div>' +
    '<div class="price-box" style="margin-top:28px">' +
      '<div><div class="from">Your price</div><div class="gst" id="gstLabel">inc. GST &amp; delivery</div></div>' +
      '<div class="amount grad-text" id="price">—</div>' +
    '</div>' +
    '<div id="priceNote" style="font-size:13px;color:#6f6961;margin-top:10px">Loading live pricing…</div>' +
    '<div id="dispatch" style="font-size:13px;color:#6b6560;margin-top:10px"></div>' +
    '<div style="display:flex;gap:12px;margin-top:18px;flex-wrap:wrap">' +
      '<a class="btn btn-aura" id="orderCta" href="quote.html?product=' + SLUG + '" style="flex:1;text-align:center;min-width:220px">Get a quote &amp; upload artwork →</a>' +
    '</div>' +
    '<p style="font-size:12.5px;color:#6f6961;margin-top:12px">Prices are read live from our current supplier rates and include GST and standard delivery Australia-wide.</p>';

  var priceEl = host.querySelector('#price'), noteEl = host.querySelector('#priceNote'),
      gstEl = host.querySelector('#gstLabel'), cta = host.querySelector('#orderCta'),
      qtyGroup = host.querySelector('#qtyGroup');

  /* Count-up is decoration only: true value is written immediately when the
     tab is hidden and re-asserted on a timer (background-tab rAF throttle). */
  function setPrice(v) { priceEl.textContent = '$' + Math.round(v).toLocaleString(); }
  function animateTo(v) {
    var start = parseFloat(priceEl.dataset.v) || 0;
    priceEl.dataset.v = v;
    if (document.hidden || start === v) { setPrice(v); return; }
    var t0 = performance.now(), done = false;
    setTimeout(function () { if (!done && parseFloat(priceEl.dataset.v) === v) setPrice(v); }, 400);
    (function tick(t) {
      if (parseFloat(priceEl.dataset.v) !== v) return;
      var k = Math.min(1, (t - t0) / 300);
      setPrice(start + (v - start) * k);
      if (k < 1) requestAnimationFrame(tick); else { done = true; setPrice(v); }
    })(performance.now());
  }

  function specText(sel, qty) {
    return AXES.map(function (a) { return a[1] + ': ' + sel[a[0]]; }).join(', ') + ', Qty: ' + qty;
  }
  function quoteHref(sel, qty, price) {
    var u = 'quote.html?product=' + SLUG + '&qty=' + qty +
            '&spec=' + encodeURIComponent(specText(sel, qty));
    if (price) u += '&price=' + price.toFixed(2);
    return u;
  }
  function showPoa(sel, qty, message) {
    priceEl.dataset.v = 0; priceEl.textContent = 'POA'; gstEl.textContent = '';
    noteEl.innerHTML = message + ' <a href="' + quoteHref(sel, qty) + '">Get a fast quote →</a>';
    cta.href = quoteHref(sel, qty);
  }

  /* Quantity buttons follow the breaks available for the selected combo. */
  function renderQtys(sel) {
    var row = GRID && GRID[comboKey(sel)];
    var qtys = row ? Object.keys(row).map(Number).sort(function (a, b) { return a - b; }) : [];
    if (!qtys.length) { qtyGroup.innerHTML = '<span style="font-size:13px;color:#6f6961">Priced by quote for this combination.</span>'; return null; }
    var want = currentQty() || startQty || qtys[0];
    var active = qtys.indexOf(want) >= 0 ? want : nearest(want, qtys);
    qtyGroup.innerHTML = qtys.map(function (q) {
      return '<button class="opt' + (q === active ? ' on' : '') + '" data-val="' + q + '">' + q.toLocaleString() + '</button>';
    }).join('');
    return active;
  }

  function calc() {
    var sel = currentSel();
    if (loadFailed) { showPoa(sel, currentQty() || startQty, 'Live pricing is unavailable right now.'); return; }
    if (!GRID) return; // still loading

    var qty = renderQtys(sel);
    if (qty === null) { showPoa(sel, startQty || 1, 'That combination isn’t online yet.'); return; }

    var row = GRID[comboKey(sel)][qty];
    var dollars = row.cents / 100;
    gstEl.textContent = 'inc. GST & delivery';
    animateTo(dollars);
    noteEl.innerHTML = row.source === 'real'
      ? '<span style="color:#2f7d4f;font-weight:700">✓ Confirmed price.</span> This is what you pay.'
      : '<b>Indicative price.</b> We confirm it on your proof, before you pay.';
    cta.href = quoteHref(sel, qty, dollars);
  }

  host.addEventListener('click', function (e) {
    var btn = e.target.closest('.opt'); if (!btn) return;
    var g = btn.closest('.opts');
    g.querySelectorAll('.opt').forEach(function (o) { o.classList.remove('on'); });
    btn.classList.add('on');
    calc();
  });

  /* ---- live price feed: one fetch per page load ---- */
  (function load() {
    if (!CFG.supabaseUrl || !CFG.supabaseKey) { loadFailed = true; calc(); return; }
    var url = CFG.supabaseUrl + '/rest/v1/product_prices?product_slug=eq.' + encodeURIComponent(SLUG) +
              '&select=options,qty,price_cents,source&limit=2000';
    fetch(url, { headers: { apikey: CFG.supabaseKey, Authorization: 'Bearer ' + CFG.supabaseKey } })
      .then(function (r) { if (!r.ok) throw new Error('price feed ' + r.status); return r.json(); })
      .then(function (rows) {
        var map = {};
        rows.forEach(function (r) {
          var key = AXES.map(function (a) { return r.options[a[0]]; }).join('|');
          (map[key] = map[key] || {})[r.qty] = { cents: r.price_cents, source: r.source };
        });
        GRID = map; calc();
      })
      .catch(function (err) {
        console.error('[aura] live pricing failed:', err);
        loadFailed = true; calc();
      });
  })();

  /* Dispatch estimate: 2 clear business days, 11am cutoff, skips weekends. */
  function isWeekend(d) { return d.getDay() === 0 || d.getDay() === 6; }
  function addBiz(from, n) { var d = new Date(from); while (n > 0) { d.setDate(d.getDate() + 1); if (!isWeekend(d)) n--; } return d; }
  function fmt(d) {
    var wd = ['Sun','Mon','Tue','Wed','Thu','Fri','Sat'], mo = ['Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec'];
    return wd[d.getDay()] + ' ' + d.getDate() + ' ' + mo[d.getMonth()];
  }
  (function () {
    var now = new Date(), start = new Date(now), past = now.getHours() >= 11;
    if (isWeekend(now) || past) start = addBiz(now, 1);
    var disp = addBiz(start, 2), before = !isWeekend(now) && !past;
    host.querySelector('#dispatch').innerHTML = '🚚 ' + (before
      ? 'Approved artwork in before <b>11am today</b> for estimated dispatch <b>' + fmt(disp) + '</b>.'
      : 'Estimated dispatch <b>' + fmt(disp) + '</b> (2 business days from next working day).');
  })();
})();
