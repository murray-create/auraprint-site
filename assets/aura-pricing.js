/* AURA PRINT & PROMO - shared live-price configurator.
   Generalisation of the business-card pattern (bc-configurator.js) for any
   product with a row set in Supabase public.product_prices.

   PRICING IS LIVE. Prices are read at page load from product_prices
   (anon SELECT only, RLS). No prices live in this file or in any page.
   Wholesale cost + supplier live in the staff-only product_price_audit table
   and never reach the browser. Rows are generated by
   pricing-engine/tools/generate-product-prices.py (single source of truth).

   source: real = reconciled against an actual supplier invoice.
           estimate = derived from harvested supplier rates; shown as an
           indicative price, confirmed on the proof before payment.

   Missing combination or feed failure -> POA + quote link. Never invent a price.

   Usage on a product page:
     <div id="aura-config" data-product="corflute-signs"
          data-axes='[["size","Size",["600x450mm","600x900mm","900x600mm","900x1200mm","1200x900mm"]],
                      ["thickness","Thickness",["3mm","5mm"]],
                      ["sides","Sides",["Single","Double"]]]'
          data-default='{"size":"900x600mm","thickness":"5mm","sides":"Single"}'
          data-qty="1"></div>
     <script src="assets/aura-pricing.js"></script>

   STYLE MODE (one page, several supplier products, each with its own axes):
     <div id="aura-config" data-product="a-frames"
          data-styles='[{"name":"Signflute A-Frame","axes":[["size","Size",[...]],...],"default":{...}}, ...]'
          data-qty="1"></div>
   Rows in product_prices carry a "style" key in options; picking a style
   swaps the option axes below it and re-keys the price grid.

   Quantity buttons adapt to whatever breaks exist for the selected combo. */
(function () {
  var CFG = window.AURA_CONFIG || {};
  var host = document.getElementById('aura-config');
  if (!host) return;

  var SLUG = host.getAttribute('data-product');
  var STYLES = JSON.parse(host.getAttribute('data-styles') || 'null');     // [{name, axes, default}]
  var AXES = JSON.parse(host.getAttribute('data-axes') || '[]');           // [key, label, [values]]
  var DEF  = JSON.parse(host.getAttribute('data-default') || '{}');
  var startQty = parseInt(host.getAttribute('data-qty'), 10) || 0;
  var LINKS = JSON.parse(host.getAttribute('data-quote-links') || '[]');   // [{label, note}] escape hatches -> quote.html

  var GRID = null;        // key -> { qty: {cents, source} }
  var loadFailed = false;

  function styleDef(name) {
    for (var i = 0; STYLES && i < STYLES.length; i++) if (STYLES[i].name === name) return STYLES[i];
    return null;
  }
  function currentStyle() {
    var b = host.querySelector('.opts[data-group="style"] .on');
    return b ? b.dataset.val : (STYLES ? STYLES[0].name : null);
  }
  function axesFor(styleName) {
    if (!STYLES) return AXES;
    var d = styleDef(styleName);
    return d ? d.axes : [];
  }
  function keyForOptions(opts) {
    if (STYLES) {
      var d = styleDef(opts.style);
      if (!d) return null;
      return opts.style + '|' + d.axes.map(function (a) { return opts[a[0]]; }).join('|');
    }
    return AXES.map(function (a) { return opts[a[0]]; }).join('|');
  }
  function comboKey(sel) { return keyForOptions(sel); }
  function currentSel() {
    var sel = {};
    if (STYLES) {
      sel.style = currentStyle();
      var d = styleDef(sel.style) || { axes: [], default: {} };
      d.axes.forEach(function (a) {
        var b = host.querySelector('.opts[data-group="' + a[0] + '"] .on');
        sel[a[0]] = b ? b.dataset.val : d.default[a[0]];
      });
      return sel;
    }
    AXES.forEach(function (a) {
      var b = host.querySelector('.opts[data-group="' + a[0] + '"] .on');
      sel[a[0]] = b ? b.dataset.val : DEF[a[0]];
    });
    return sel;
  }
  function nearest(target, arr) {
    return arr.reduce(function (a, b) { return Math.abs(b - target) < Math.abs(a - target) ? b : a; });
  }

  function optBtns(group, values, activeVal) {
    return '<div class="opts" data-group="' + group + '">' + values.map(function (v) {
      return '<button class="opt' + (String(v) === String(activeVal) ? ' on' : '') + '" data-val="' + v + '">' + v + '</button>';
    }).join('') + '</div>';
  }

  host.innerHTML =
    (STYLES
      ? '<div class="optlabel">Style</div>' + optBtns('style', STYLES.map(function (s) { return s.name; }), STYLES[0].name) +
        '<div id="subAxes"></div>'
      : AXES.map(function (a) {
          return '<div class="optlabel">' + a[1] + '</div>' + optBtns(a[0], a[2], DEF[a[0]]);
        }).join('')) +
    '<div class="optlabel">Quantity</div><div class="opts" data-group="qty" id="qtyGroup"></div>' +
    '<div id="customQtyWrap" style="margin-top:12px;display:flex;align-items:center;gap:10px;flex-wrap:wrap">' +
      '<label for="customQty" style="font-size:13px;color:#6f6961">Or enter your own quantity:</label>' +
      '<input id="customQty" type="number" min="1" step="1" inputmode="numeric" placeholder="Qty" ' +
      'style="width:96px;padding:9px 11px;border:1px solid #d8d2ca;border-radius:9px;font-size:14px;font-family:inherit" />' +
    '</div>' +
    '<div class="price-box" style="margin-top:28px">' +
      '<div><div class="from">Your price</div><div class="gst" id="gstLabel">inc. GST &amp; delivery</div></div>' +
      '<div class="amount grad-text" id="price">—</div>' +
    '</div>' +
    '<div id="priceNote" style="font-size:13px;color:#6f6961;margin-top:10px">Loading live pricing…</div>' +
    '<div id="dispatch" style="font-size:13px;color:#6b6560;margin-top:10px"></div>' +
    '<div style="display:flex;gap:12px;margin-top:18px;flex-wrap:wrap">' +
      '<a class="btn btn-aura" id="orderCta" href="quote.html?product=' + SLUG + '" style="flex:1;text-align:center;min-width:220px">Get a quote &amp; upload artwork →</a>' +
    '</div>' +
    (LINKS.length ? '<div id="quoteLinks" style="display:flex;gap:18px;margin-top:12px;flex-wrap:wrap">' +
      LINKS.map(function (l, i) {
        return '<a class="qlink" data-i="' + i + '" href="quote.html?product=' + SLUG + '" ' +
               'style="font-size:13.5px;font-weight:600;color:#7a4bd6;text-decoration:underline;text-underline-offset:3px">' + l.label + '</a>';
      }).join('') + '</div>' : '') +
    '<p style="font-size:12.5px;color:#6f6961;margin-top:12px">Prices are read live from our current supplier rates and include GST and standard delivery Australia-wide.</p>';

  var priceEl = host.querySelector('#price'), noteEl = host.querySelector('#priceNote'),
      gstEl = host.querySelector('#gstLabel'), cta = host.querySelector('#orderCta'),
      qtyGroup = host.querySelector('#qtyGroup');

  /* Style mode: (re)draw the selected style's own option axes. */
  function renderSubAxes(styleName) {
    if (!STYLES) return;
    var d = styleDef(styleName);
    host.querySelector('#subAxes').innerHTML = (d ? d.axes : []).map(function (a) {
      return '<div class="optlabel">' + a[1] + '</div>' + optBtns(a[0], a[2], d.default[a[0]]);
    }).join('');
  }
  renderSubAxes(STYLES && STYLES[0].name);

  /* Count-up is decoration only: true value is written immediately when the
     tab is hidden and re-asserted on a timer (background-tab rAF throttle). */
  function setPrice(v) { priceEl.textContent = '$' + Math.round(v).toLocaleString(); }
  function animateTo(v) {
    var start = parseFloat(priceEl.dataset.v) || 0;
    priceEl.dataset.v = v;
    if (document.hidden || start === v) { setPrice(v); return; }
    var t0 = performance.now(), done = false;
    setTimeout(function () { if (!done && parseFloat(priceEl.dataset.v) === v) setPrice(v); }, 400);
    (function tick(t) {
      if (parseFloat(priceEl.dataset.v) !== v) return;
      var k = Math.min(1, (t - t0) / 300);
      setPrice(start + (v - start) * k);
      if (k < 1) requestAnimationFrame(tick); else { done = true; setPrice(v); }
    })(performance.now());
  }

  function specText(sel, qty) {
    var parts = [];
    if (STYLES) parts.push('Style: ' + sel.style);
    axesFor(sel.style).forEach(function (a) { parts.push(a[1] + ': ' + sel[a[0]]); });
    return parts.join(', ') + ', Qty: ' + qty;
  }
  function quoteHref(sel, qty, price) {
    var u = 'quote.html?product=' + SLUG + '&qty=' + qty +
            '&spec=' + encodeURIComponent(specText(sel, qty));
    if (price) u += '&price=' + price.toFixed(2);
    return u;
  }
  /* Escape hatches (custom size / volume runs) always carry the current spec. */
  function updateQuoteLinks(sel, qty) {
    LINKS.forEach(function (l, i) {
      var a = host.querySelector('.qlink[data-i="' + i + '"]');
      if (!a) return;
      a.href = 'quote.html?product=' + SLUG + '&qty=' + qty +
               '&spec=' + encodeURIComponent((l.note ? l.note + ' — ' : '') + specText(sel, qty));
    });
  }

  function showPoa(sel, qty, message) {
    priceEl.dataset.v = 0; priceEl.textContent = 'POA'; gstEl.textContent = '';
    noteEl.innerHTML = message + ' <a href="' + quoteHref(sel, qty) + '">Get a fast quote →</a>';
    cta.href = quoteHref(sel, qty);
    updateQuoteLinks(sel, qty);
  }

  /* Per-unit interpolation: price any quantity from the stored breaks.
     Exact break -> stored price. Between breaks -> slide the per-unit rate
     linearly between the two nearest breaks, then multiply by the quantity.
     Below the smallest / above the largest break -> clamp to that break's
     per-unit rate. */
  function priceForQty(row, qty) {
    var breaks = Object.keys(row).map(Number).sort(function (a, b) { return a - b; });
    if (!breaks.length || !(qty >= 1)) return null;
    if (row[qty]) return { cents: row[qty].cents, source: row[qty].source, exact: true };
    function unit(q) { return row[q].cents / q; }
    var lo = breaks[0], hi = breaks[breaks.length - 1], rate;
    if (qty <= lo) rate = unit(lo);
    else if (qty >= hi) rate = unit(hi);
    else {
      var a = lo, b = hi;
      for (var i = 0; i < breaks.length - 1; i++) {
        if (qty >= breaks[i] && qty <= breaks[i + 1]) { a = breaks[i]; b = breaks[i + 1]; break; }
      }
      var ra = unit(a), rb = unit(b);
      rate = ra + (rb - ra) * (qty - a) / (b - a);
    }
    return { cents: Math.round(rate * qty), source: 'estimate', exact: false };
  }

  /* Effective quantity: a valid custom entry wins, else the active preset
     button, else the starting quantity. */
  function readQty() {
    var ci = host.querySelector('#customQty');
    if (ci && ci.value.trim() !== '') {
      var n = parseInt(ci.value, 10);
      if (n >= 1) return { qty: n, custom: true };
    }
    var b = host.querySelector('.opts[data-group="qty"] .on');
    if (b) return { qty: parseInt(b.dataset.val, 10), custom: false };
    return { qty: startQty || null, custom: false };
  }

  function calc() {
    var sel = currentSel();
    if (loadFailed) { showPoa(sel, readQty().qty || 1, 'Live pricing is unavailable right now.'); return; }
    if (!GRID) return; // still loading

    var row = GRID[comboKey(sel)];
    var qtys = row ? Object.keys(row).map(Number).sort(function (a, b) { return a - b; }) : [];
    if (!qtys.length) {
      qtyGroup.innerHTML = '<span style="font-size:13px;color:#6f6961">Priced by quote for this combination.</span>';
      showPoa(sel, readQty().qty || 1, 'That combination isn’t online yet.');
      return;
    }

    var eq = readQty(), custom = eq.custom, qty = eq.qty;
    if (!custom) {
      var want = qty || qtys[0];
      qty = qtys.indexOf(want) >= 0 ? want : nearest(want, qtys);
    }
    /* Preset buttons always show the break points; highlight one only when the
       customer is on a preset (not typing their own number). */
    qtyGroup.innerHTML = qtys.map(function (q) {
      return '<button class="opt' + (!custom && q === qty ? ' on' : '') + '" data-val="' + q + '">' + q.toLocaleString() + '</button>';
    }).join('');

    if (!(qty >= 1)) { showPoa(sel, 1, 'Enter a quantity to see your price.'); return; }

    /* Above our largest stored break we have no supplier rate to trust, so we
       route to a custom quote instead of extrapolating a price. */
    var maxBreak = qtys[qtys.length - 1];
    if (qty > maxBreak) {
      showPoa(sel, qty, 'Over ' + maxBreak.toLocaleString() + ' is priced by custom quote.');
      return;
    }

    var p = priceForQty(row, qty);
    if (!p) { showPoa(sel, qty, 'That combination isn’t online yet.'); return; }
    var dollars = p.cents / 100;
    gstEl.textContent = 'inc. GST & delivery';
    animateTo(dollars);
    if (p.exact) {
      noteEl.innerHTML = p.source === 'real'
        ? '<span style="color:#2f7d4f;font-weight:700">✓ Confirmed price.</span> This is what you pay.'
        : '<b>Indicative price.</b> We confirm it on your proof, before you pay.';
    } else {
      noteEl.innerHTML = '<b>Indicative price for ' + qty.toLocaleString() + '.</b> Worked out from our price breaks; we confirm it on your proof, before you pay.';
    }
    cta.href = quoteHref(sel, qty, dollars);
    updateQuoteLinks(sel, qty);
  }

  host.addEventListener('click', function (e) {
    var btn = e.target.closest('.opt'); if (!btn) return;
    var g = btn.closest('.opts');
    g.querySelectorAll('.opt').forEach(function (o) { o.classList.remove('on'); });
    btn.classList.add('on');
    /* Choosing a preset break clears any custom quantity the customer typed. */
    if (g.getAttribute('data-group') === 'qty') { var ci = host.querySelector('#customQty'); if (ci) ci.value = ''; }
    if (STYLES && g.getAttribute('data-group') === 'style') renderSubAxes(btn.dataset.val);
    calc();
  });

  /* Typing a custom quantity deselects the preset buttons and reprices live. */
  var customInput = host.querySelector('#customQty');
  if (customInput) {
    customInput.addEventListener('input', function () {
      if (this.value.trim() !== '') {
        host.querySelectorAll('.opts[data-group="qty"] .opt').forEach(function (o) { o.classList.remove('on'); });
      }
      calc();
    });
  }

  /* ---- live price feed: one fetch per page load ---- */
  (function load() {
    if (!CFG.supabaseUrl || !CFG.supabaseKey) { loadFailed = true; calc(); return; }
    var url = CFG.supabaseUrl + '/rest/v1/product_prices?product_slug=eq.' + encodeURIComponent(SLUG) +
              '&select=options,qty,price_cents,source&limit=2000';
    fetch(url, { headers: { apikey: CFG.supabaseKey, Authorization: 'Bearer ' + CFG.supabaseKey } })
      .then(function (r) { if (!r.ok) throw new Error('price feed ' + r.status); return r.json(); })
      .then(function (rows) {
        var map = {};
        rows.forEach(function (r) {
          var key = keyForOptions(r.options);
          if (key === null) return;   // row for a style this page doesn't know
          (map[key] = map[key] || {})[r.qty] = { cents: r.price_cents, source: r.source };
        });
        GRID = map; calc();
      })
      .catch(function (err) {
        console.error('[aura] live pricing failed:', err);
        loadFailed = true; calc();
      });
  })();

  /* Dispatch estimate: 2 clear business days, 11am cutoff, skips weekends. */
  function isWeekend(d) { return d.getDay() === 0 || d.getDay() === 6; }
  function addBiz(from, n) { var d = new Date(from); while (n > 0) { d.setDate(d.getDate() + 1); if (!isWeekend(d)) n--; } return d; }
  function fmt(d) {
    var wd = ['Sun','Mon','Tue','Wed','Thu','Fri','Sat'], mo = ['Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec'];
    return wd[d.getDay()] + ' ' + d.getDate() + ' ' + mo[d.getMonth()];
  }
  (function () {
    var now = new Date(), start = new Date(now), past = now.getHours() >= 11;
    if (isWeekend(now) || past) start = addBiz(now, 1);
    var disp = addBiz(start, 2), before = !isWeekend(now) && !past;
    host.querySelector('#dispatch').innerHTML = '🚚 ' + (before
      ? 'Approved artwork in before <b>11am today</b> for estimated dispatch <b>' + fmt(disp) + '</b>.'
      : 'Estimated dispatch <b>' + fmt(disp) + '</b> (2 business days from next working day).');
  })();
})();
